function retrive_label_color_code(name){
    var label_color_code = '255 255 0'
    var label_data = atlas_json();
    for (var i = 0; i < label_data.length; i++) {
        if(label_data[i].Name.indexOf(name) != -1 || name == label_data[i].Name){
            label_color_code = label_data[i].Color;
        }
    };
    return label_color_code;
}

function retrive_label_name(name){
    var label_name_code;
    var label_data = atlas_json();
    name = name.replace('RH:_', '');
    for (var i = 0; i < label_data.length; i++) {
        if(label_data[i].Name.search(name) != -1){
            label_name_code = label_data[i].Name;
            label_name_code = label_name_code.replace('RH:_', '');
            return label_name_code;
        }else{
            label_name_code = name;
        }
    };
    return label_name_code;
} 

function check_input_label_name(name){
    var label_name_code = 0;
    var label_data = atlas_json();
    for (var i = 0; i < label_data.length; i++) {
        if(label_data[i].Name.search(name) != -1 || name == label_data[i].Name){
            label_name_code = 1;
        }
    };
    return label_name_code;
}   
